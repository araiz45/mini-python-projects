# Operators in python
# There are six types of operator in python
# 1 - Addition (+)
# 2 - Subraction (-)
# 3 - Multiplication (*)
# 4 - Division (/)
# 5 - Exponenetial (**)
# 6 - Floor Division (//)

print("5 + 2 is ", 5 + 2)
print("5 - 2 is ", 5 + 3)
print("5 * 2 is ", 5 * 2)
print("5 / 2 is ", 5 / 2)
print("5 ** 2 is ", 5 ** 2)
print("5// 2 is ", 5 // 2)
